package ui.copypaste.paste.classes;

public class TagClipCombo {

    private String tagName;
    private int tagId;

    public TagClipCombo(String tagName, int tagId) {
        this.tagName = tagName;
        this.tagId = tagId;
    }

    public String getTagName() {
        return tagName;
    }

    public int getTagId() {
        return tagId;
    }


}
